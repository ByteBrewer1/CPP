class Solution{
public:	
	/* Function to replace every element with the
	next greatest element */
	void nextGreatest(int arr[], int n) {
        // replace every element with the next greatest element
        for(int i=0;i<n;i++){
            int j=i+1;
            while(j<n && arr[j]<arr[i]){
                swap(arr[i],arr[j]);
                j++;
            }
        }
	}


};

