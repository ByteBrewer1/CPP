class Solution{
    public:
        int minimumTime(int n,int cur,vector<int> &pos,vector<int> &time){
        for(int i=0;i<n;i++){
            pos[i]=abs(pos[i]-cur);
        }
        int min=INT_MAX;
        for(int i=0;i<n;i++){
            if((pos[i]*time[i])<min){
                min=pos[i]*time[i];
                }
            }
            return min;
        }

};



